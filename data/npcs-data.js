// === –ì–ï–ù–ï–†–ê–¶–ò–Ø –°–õ–£–ß–ê–ô–ù–´–• NPC ===
function generateRandomNPC(template, id) {
    const name = template.namePool
        ? template.namePool[Math.floor(Math.random() * template.namePool.length)]
        : template.name;

    const age = template.ageRange
        ? Math.floor(Math.random() * (template.ageRange[1] - template.ageRange[0])) + template.ageRange[0]
        : template.age;

    const meditationHours = template.meditationHoursRange
        ? Math.random() * (template.meditationHoursRange[1] - template.meditationHoursRange[0]) + template.meditationHoursRange[0]
        : template.meditationHours;

    const level = template.levelRange
        ? Math.floor(Math.random() * (template.levelRange[1] - template.levelRange[0])) + template.levelRange[0]
        : template.level;

    return {
        id: `${template.id}_${id}`,
        name: name,
        title: template.title,
        age: age,
        meditationHours: Math.round(meditationHours * 10) / 10,
        level: level,
        skin: template.skin,
        emoji: template.emoji,
        color: template.color,
        rarity: template.rarity,
        dialoguePool: template.dialoguePool,
        quotes: template.quotes || null,
        zone: template.zone || 'center'
    };
}

